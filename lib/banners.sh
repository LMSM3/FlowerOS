# Strip ANSI codes for length calculation
_fos_strip_ansi() {
    echo -e "$1" | sed 's/\x1b\[[0-9;]*m//g'
}

# Pad string to fixed width (visual padding, ignoring ANSI)
_fos_pad() {
    local text="$1"
    local width="${2:-$_FOS_WIDTH}"
    local stripped
    stripped=$(_fos_strip_ansi "$text")
    local visible_len=${#stripped}
    local pad=$((width - visible_len))
    if (( pad < 0 )); then pad=0; fi
    printf '%s%*s' "$text" "$pad" ""
}

# Horizontal rule: â•â•â•...â•â•â•
_fos_hrule() {
    local width="${1:-$_FOS_WIDTH}"
    printf '%*s' "$((width + 2))" '' | tr ' ' "$_FOS_H"
}

